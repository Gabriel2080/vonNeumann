public class DataTRWS {
    private int t;
    private int r;
    private int w;
    private int s;

    public DataTRWS(int t, int r, int w, int s) {
        this.t = t;
        this.r = r;
        this.w = w;
        this.s = s;
    }

    public int getT() {
        return t;
    }

    public int getR() {
        return r;
    }

    public int getW() {
        return w;
    }

    public int getS() {
        return s;
    }
    
}
