public class DataTRW {
    private int t;
    private int r;
    private int w;

    public DataTRW(int t, int r, int w) {
        this.t = t;
        this.r = r;
        this.w = w;
    }

    public int getT() {
        return t;
    }

    public int getR() {
        return r;
    }

    public int getW() {
        return w;
    }
    
}
